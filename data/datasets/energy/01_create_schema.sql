-- 01_create_schema.sql - smart-meter energy dataset schema.
-- Loads into its own ENERGY schema; the dedicated read-only MCP user reads it
-- via database-wide read (USE ANY SCHEMA + SELECT ANY TABLE), no per-schema
-- grant. Tables keep the ENERGY_ prefix so fully-qualified names stay clear.
--
-- Idempotent: CREATE OR REPLACE TABLE means this can be re-run (e.g. via
-- exakit data-load --force) without manual cleanup.

CREATE SCHEMA IF NOT EXISTS ENERGY;
OPEN SCHEMA ENERGY;

-- energy_meters (50 rows, from data/energy_meters.csv). PK: meter_id.
CREATE OR REPLACE TABLE ENERGY_METERS (
    METER_ID      DECIMAL(9,0)  NOT NULL,
    HOUSEHOLD     VARCHAR(25)   NOT NULL,
    CITY          VARCHAR(25)   NOT NULL,
    TARIFF        VARCHAR(10)   NOT NULL,   -- BASIC / GREEN / NIGHT
    BASE_LOAD_KWH DECIMAL(6,3)  NOT NULL,   -- typical hourly baseline
    CONSTRAINT ENERGY_METERS_PK PRIMARY KEY (METER_ID)
);

-- energy_readings (generated by 02_load_data.sql: 50 meters x 90 days x 24h).
-- FK (documented, not enforced): meter_id -> energy_meters.
CREATE OR REPLACE TABLE ENERGY_READINGS (
    METER_ID   DECIMAL(9,0)  NOT NULL,
    READING_TS TIMESTAMP     NOT NULL,
    KWH        DECIMAL(8,3)  NOT NULL,
    CONSTRAINT ENERGY_READINGS_PK PRIMARY KEY (METER_ID, READING_TS)
);
